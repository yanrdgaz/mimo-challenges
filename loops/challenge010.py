# Create a program that reminds us three times to stop the bot. Tasks (2): Code a while loop that prints "Reminder:
# Stop the bot!" when reminder_count is less than 3. After the print statement, increase the value of reminder_count
# by 1.

reminder_count = 0
while reminder_count < 3:
    print("Reminder: Stop the bot!")
    reminder_count += 1
